                <h4>Dicas de investimentos de baixo risco</h4>
                <ul>
                    <li><strong>Fundos DI:</strong> Fundos de investimento que aplicam principalmente em títulos públicos e outros ativos de baixo risco, geralmente atrelados ao CDI.</li>
                    <li><strong>Tesouro Selic:</strong> Título público federal de renda fixa, considerado um dos investimentos mais seguros do Brasil, ideal para reserva de emergência.</li>
                    <li><strong>LCI e LCA:</strong> Letras de Crédito Imobiliário e do Agronegócio são isentas de Imposto de Renda para pessoas físicas e também são protegidas pelo FGC.</li>
