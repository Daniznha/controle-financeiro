                <h4>CDI</h4>
                <p>O CDI (Certificado de Depósito Interbancário) é uma taxa de referência para investimentos de renda fixa. É utilizado como parâmetro para diversas aplicações financeiras no Brasil. Investimentos atrelados ao CDI, como alguns CDBs e fundos DI, costumam oferecer uma rentabilidade próxima a essa taxa.</p>
                <p>Investir em produtos que acompanham o CDI é uma forma segura de obter retorno acima da inflação, já que a taxa CDI tende a acompanhar a taxa Selic, a principal taxa de juros da economia brasileira.</p>
            </div>
            <div class="col-md-6">
                <h4>CDB</h4>
                <p>O CDB (Certificado de Depósito Bancário) é um título emitido por bancos para captar recursos. Quando você investe em um CDB, está emprestando dinheiro ao banco, que paga uma remuneração em troca. Os CDBs podem ter diferentes formas de remuneração: pré-fixados, pós-fixados (atrelados ao CDI) ou híbridos (parte pré e parte pós).</p>
                <p>Os CDBs são considerados investimentos de baixo risco, principalmente se emitidos por bancos grandes e sólidos. Além disso, são protegidos pelo Fundo Garantidor de Créditos (FGC) até o limite de R$ 250.000 por CPF por instituição financeira.</p>
            </div>
        </div>
        
        <div class="row mt-4">
